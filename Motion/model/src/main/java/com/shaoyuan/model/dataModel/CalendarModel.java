package com.shaoyuan.model.dataModel;

public class CalendarModel {
    private String name;
    private boolean today;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isToday() {
        return today;
    }

    public void setToday(boolean today) {
        this.today = today;
    }
}
